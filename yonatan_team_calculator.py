# define a function for addition
def add(num1, num2):
    ''' The function gets 2 numbers and returns the result of their addition
        :param num1: number
        :param num2: number
        :type num1: float or integer
        :type num2: float or integer
        :return: The result of the addition between the 2 numbers
        :rtype: float
    '''

    return float(num1) + float(num2)


# define a function for subtraction
def subtract(num1, num2):
    ''' The function gets 2 numbers and returns the result of their subtract
            :param num1: number
            :param num2: number
            :type num1: float or integer
            :type num2: float or integer
            :return: The result of the subtract between the 2 numbers
            :rtype: float
        '''
    return num1 - num2


# define a function for multiplication
def multiply(num1, num2):
    return num1 * num2


# define a function for division
def divide(num1, num2):
    return num1 / num2
